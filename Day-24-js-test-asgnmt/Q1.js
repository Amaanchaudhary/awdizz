//Q1.Write a JS function that takes an array of strings as input and returns a new
// array containing the length of those strings

var arr = ["hey", "my", "name", "is", "Amaan"];

function Arraylength(arr) {
    var NewArray = []
    for (var i = 0; i < arr.length; i++) {
        NewArray.push(arr[i].length);
    }
    return NewArray;
}

console.log(Arraylength(arr));

