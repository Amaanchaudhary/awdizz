//Q2.Write a JS function that takes an array of numbers as input and returns a new 
//array with only the even numbers from the original array

var arr = [1, 2, 43, 5, 5, 66, 7, 8, 22, 91, 92,]

function EvenArray(arr){
    var NewArr = []
    for(var i = 0 ; i < arr.length; i++){
        if(arr[i] % 2 == 0){
            NewArr.push(arr[i])
        }
    }
    return NewArr
}
console.log(EvenArray(arr));

